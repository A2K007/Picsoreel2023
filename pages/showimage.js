import Head from "next/head"
import { useState, useEffect } from 'react'
import { useRouter } from "next/router";
import Link from "next/link";


export default function Scanner() {
    const [images, setImages] = useState([])
    const getimage = async locid => {
        const s = process.env.BASE_FETCH_URL
        const res = await fetch(`http://localhost:3000/api/getimage/${locid}`)
        const data = await res.json()
        // console.log(data)
        setImages(data)
    }

    const voteit = async imgid => {
        try {
            const userid = localStorage.getItem('user')
            const res = await fetch(`http://localhost:3000/api/addvote/${imgid},${userid}`)
            const data = await res.json()

        }
        catch (e) {
            alert("Already Voted")
        }

    }
    var router = useRouter();
    // console.log(id)
    // getimage(id)
    var id = "no"
    id = router.query["id"];
    getimage(id)
    return (
        <>
            <Head>
                <title>Image Display</title>
                <meta name="description" content="Generated by create next app" />
                <meta name="viewport" content="width=device-width, initial-scale=1" />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <div>
                <h1>This is image display page</h1>
                {
                    images.map((image) => {
                        return (
                            <>
                                <div key={image.image_id}>
                                    {image.name} | {image.class} |
                                    <img src={image.url}></img>
                                </div>


                                <button onClick={() => voteit(image.image_id)}>Add to wishlist</button>
                                <Link href='/scanner'><button>No</button></Link>
                            </>
                        )
                    })
                }





            </div>
        </>

    )
}